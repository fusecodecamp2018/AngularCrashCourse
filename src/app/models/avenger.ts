export class Avenger {
  name: string;
  realName: string;
  primaryColor: string;
  icon: string;
  wikiaUrl: string;
  constructor(name: string, realName: string, primaryColor: string, icon: string, wikiaUrl: string) {
    this.name = name;
    this.realName = realName;
    this.primaryColor = primaryColor;
    this.icon = icon;
    this.wikiaUrl = wikiaUrl;
  }
}
